/*  text to graphics image generation routine 
    text_to_image
    DESCRIPTION : First the function breaks down a char[] into a buffer holding centered
                  text ascii data in a fixed array with the size of a fully filled status bar,
                  then it makes a rastorized image buffer of the status bar using the centered string.
    INPUT : char input[]-- string to be drawn on the image
            unsigned char* status_bar_image-- an empty image buffer the size of the status bar to be filled with
            the correct status_bar based on the passed input string
    OUTPUT : a char* buffer of fixed size containing the data to the filled status bar(with the correct text)
             (NOTE THIS IS THE OUTPUT IN THE PASSED status_bar_image)
    RETURN : none
    SIDE EFFECTS : none
*/
void text_to_image(char input[], unsigned char* status_bar_img){
    int ascii_buffer[40];
    int length = strlen(input);
    if (length > 40){
        //string too long, so truncate anything beyond the 39th index
        length = 40;
    }
    int startpoint = (40-length)/2;
    /* width of status bar is 320 pixels and each character is 8 pixels wide... 
       not considering space between characters, 320/8 = 40 characters maximum (the magic number)
       Centering, let 1 char strings be placed on character 19(indexed but the 2th char) to begin, then add to right, left, right...*/
    int i;
    for (i = 0; i < 40; i++){
        // (int) casting will make a character its integer ascii value equivalent
        if ( i < startpoint || i > startpoint + length){
            ascii_buffer[i] = (int)(' '); //ascii for a space{empty}
        }
        else{
            ascii_buffer[i] = (int)input[i-startpoint];
        }
    }
    /* Now the ascii_buffer is full of ascii values in order of how to set them 
       We just need to fill the buffer accordingly now */
    int inner_index;
    int row;
    int char_index;
    for(row = 0; row < SBAR_HEIGHT; row++){
        for(char_index = 0; char_index < 40; char_index++){ //each character
            for(inner_index = 0; inner_index < 8; inner_index++){ //8 wide index of the row in each character(width of a character)
                if(row == 0 || row > 16){ // 16 is the height of a character 
                    status_bar_img[(char_index*8)+inner_index+row*IMAGE_X_DIM] = 0x08; //outside of range is the background color
                }
                else{
                    char row_byte = font_data[ascii_buffer[char_index]][row-1]; //get the character graphic
                    unsigned int foreground;
                    switch (inner_index) {  //find if the particular bit should be foreground
                        case 0:
                            foreground = row_byte & 0x01;
                            break;
                        case 1:
                            foreground = row_byte & 0x02;
                            break;
                        case 2:
                            foreground = row_byte & 0x04;
                            break;
                        case 3:
                            foreground = row_byte & 0x08;
                            break;
                        case 4:
                            foreground = row_byte & 0x10;
                            break;
                        case 5:
                            foreground = row_byte & 0x20;
                            break;
                        case 6:
                            foreground = row_byte & 0x40;
                            break;
                        case 7:
                            foreground = row_byte & 0x80;
                            break;
                    }
                    status_bar_img[(char_index*8)+(8-inner_index)+row*IMAGE_X_DIM] = (foreground) ? 0x12 : 0x08; //foreground and background colors, respectively
                }
            }
        }
    }
    //now the full size image is full with the correct characters in the status bar
    return;
}

/*  
    rastor_to_plane
    DESCRIPTION : Takes a rastorized image buffer and converts it into a VGA plane based image buffer than can be
                  copied properly into video memory.
    INPUT : input-- the rastorized image buffer to be modified
    OUTPUT : input again-- but now its a 4-plane based image buffer
    RETURN : none
    SIDE EFFECTS : none
*/
void rastor_to_plane(unsigned char* input){
    /* plane order is 0-1-2-3 */
    unsigned char rastor_image[IMAGE_X_DIM*SBAR_HEIGHT];
    int ind;
    for (ind = 0; ind < IMAGE_X_DIM*SBAR_HEIGHT; ind++){
        rastor_image[ind] = input[ind];
    }
    int i;
    for (i = 0; i < IMAGE_X_DIM * SBAR_HEIGHT; i++){
        input[SCROLL_X_WIDTH*SBAR_HEIGHT*(i%4) + i/4] = rastor_image[i];
    }
    //now input is rewritten into vga plane based buffer order
    return;
}
